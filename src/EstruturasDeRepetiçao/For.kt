package EstruturasDeRepetiçao
//Repetir um numero somando 1 acacada repetiçao como por exemplo repete o numero 0 e na outra repetiçao adiciona o numero 1 ou seja 0 + 1 = 1 e se repete.
fun main (){
    var num1: Int = 0
    for (num1 in 0 ..100)
        println(num1)
        num1++
}
