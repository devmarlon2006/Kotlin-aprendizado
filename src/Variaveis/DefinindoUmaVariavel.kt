package Variaveis

//Como definir uma variavel? primeiro usamos ("'val' para variaveis imutaveis ou 'var' para mutaveis")
//segundo damos um nome para nossa variavel, a variavel não pode conter caracteres especiais em nehum lugar(com exeçao do '_'),
//nem numeros no começo, apenas no final
//depois de finimos seu tipo ou não depois o seu valor

fun main(){
    val numero: Int = 10 //declarar uma variavel definindo seu tipo!
    val numero2 = 10 // declarando uma variavel sem definir seu tipo!
    println(numero)
    println(numero2) // imprimir o valor da variavel!
}