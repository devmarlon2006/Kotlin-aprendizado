package Variaveis

fun variaveis(){
    //declarar uma variavel definindo seu tipo!
    val numero: Int = 10
    //declarar uma variavael sem precisar definir seu tipo!
    val numer = 10
    //mostrar valores, o cifrao($)e usado para colocar uma variavel em uma string
    println("$numero e $numer")
    //soma de variaveis
    println(numero + numer)
}



fun main(){
    variaveis()
}